    private boolean fieldIsValid(javax.swing.JTextField... fields) {
        boolean isValid = true;
        for (javax.swing.JTextField field : fields) {
            if (field.getText() == null || field.getText().isEmpty()) {
                isValid = false;
            }
        }
        return isValid;
    }
    
