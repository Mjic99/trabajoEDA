    private void addUserButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addUserButtonActionPerformed
        if (fieldIsValid(userField, passwordField)) {
            Usuario user = new Usuario(userField.getText(), passwordField.getText());
            userManager.agregarUsuario(user);
            System.out.println(user.toString());
            userField.setText("");
            passwordField.setText("");
        } else {
            JOptionPane.showMessageDialog(null, "Información incompleta");
        }
    }//GEN-LAST:event_addUserButtonActionPerformed

    private void sendMessageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendMessageActionPerformed
        if (fieldIsValid(destinationUser, senderUser, dateField)){
            userManager.enviarMensaje(senderUser.getText(), destinationUser.getText(), new Mensaje(messageField.getText(),dateField.getText()));
            destinationUser.setText("");
            messageField.setText("");
        } else {
            JOptionPane.showMessageDialog(null, "Información incompleta");
        }
    }//GEN-LAST:event_sendMessageActionPerformed

    private void seeUserTableActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_seeUserTableActionPerformed
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Nombre");
        modelo.addColumn("Password");
        modelo.addColumn("Mensajes en buzón");
        NodoLEG<Usuario> ptr = userManager.getNodo();
        while(ptr!= null){
            String[] row = {ptr.getInfo().getNombre(),ptr.getInfo().getPassword(),String.valueOf(ptr.getInfo().getCantMensajes())};
            modelo.insertRow(0, row);
            ptr=ptr.getSiguiente();
        }
        userTable.setModel(modelo);
    }//GEN-LAST:event_seeUserTableActionPerformed

    private void seeMessagesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_seeMessagesActionPerformed
        if (fieldIsValid(userLoginField, passwordLoginField)) {
            NodoLEG<Usuario> ptr = userManager.getNodo();
            boolean loggedIn = false;
            while (ptr != null) {
                if (ptr.getInfo().getNombre().equals(userLoginField.getText()) && ptr.getInfo().getPassword().equals(passwordLoginField.getText())) {
                    Mensaje mensaje = ptr.getInfo().eliminarDeCola(true);
                    JOptionPane.showMessageDialog(null, mensaje != null ? mensaje.toString() : "¡No hay mensajes!");
                    loggedIn = true;
                }
                ptr = ptr.getSiguiente();
            }
            if (!loggedIn) {
                JOptionPane.showMessageDialog(null, "Usuario incorrecto");
            }
        }
    }//GEN-LAST:event_seeMessagesActionPerformed

